        ("Option 2: Clean Minimalist", get_form16x_option_2_clean_minimalist(), "Simple, compact, works on all terminals"),
        ("Option 3: Bold Block", get_form16x_option_3_bold_block(), "Impactful, enterprise-ready, with tagline"),
        ("Option 4: Corporate Professional", get_form16x_option_4_corporate_professional(), "Business-focused, clear lettering"),
